// cambiar todos los contenidos a la vez
function changeContent(index) {
    const contentTop = document.getElementById('content-top');
    const contentBottom = document.getElementById('content-bottom');
    const tituloSobreMi = document.getElementById('titulo-sobre-mi');
    const tituloLoves = document.getElementById('titulo-loves');
    const rectangleTop = document.querySelector('.rectangle-5');
    const gfImage = document.getElementById('gf-image');
    
    // Cambiar contenido de los contenedores
    contentTop.innerHTML = topContents[index];
    contentBottom.innerHTML = bottomContents[index];
    
    // Cambiar textos superiores
    tituloSobreMi.textContent = titulosSobreMi[index];
    tituloLoves.textContent = titulosLoves[index];

    const isFourth = index === 3;
    if (tituloSobreMi) {
        tituloSobreMi.style.display = isFourth ? 'none' : '';
    }
    if (contentTop) {
        contentTop.style.display = isFourth ? 'none' : '';
    }
    if (gfImage) {
        gfImage.style.display = isFourth ? 'block' : 'none';
    }
    if (rectangleTop) {
        rectangleTop.classList.toggle('gf-mode', isFourth);
    }
    if (tituloLoves) {
        tituloLoves.classList.toggle('gf-left', isFourth);
    }
    
    // Mover título "sobre mi" a la derecha cuando es BYF (index 1)
    const root = document.documentElement;
    if (index === 1) {
        root.style.setProperty('--titulo-sobre-mi-left', '920px');
    } else {
        root.style.setProperty('--titulo-sobre-mi-left', '880px');
    }
    
    // Actualizar botones activos
    const buttons = document.querySelectorAll('.heart-buttons-bottom .heart-btn');
    buttons.forEach((btn, i) => {
        btn.classList.toggle('active', i === index);
    });
    
    // Añadir efecto de animación a todo
    contentTop.style.animation = 'fadeIn 0.5s ease';
    contentBottom.style.animation = 'fadeIn 0.5s ease';
    tituloSobreMi.style.animation = 'fadeIn 0.5s ease';
    tituloLoves.style.animation = 'fadeIn 0.5s ease';
    
    setTimeout(() => {
        contentTop.style.animation = '';
        contentBottom.style.animation = '';
        tituloSobreMi.style.animation = '';
        tituloLoves.style.animation = '';
    }, 500);
}

